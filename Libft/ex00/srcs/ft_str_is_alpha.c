#include "../includes/libft.h"

int	ft_str_is_alpha(char	*str)
{
	int	len;

	len = 0;
	while (str[len])
	{
		if (!((str[len] > 64 && str[len] < 91)
				|| (str[len] > 96 && str[len] < 123)))
			return (0);
		len++;
	}
	return (1);
}
